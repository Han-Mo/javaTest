public class Displacementoperator {
    public static void main(String[] args) {
        int a = 32;
        System.out.println(a >> 1);
        System.out.println(a >> 2);
        System.out.println(a >> 3);
        System.out.println(a >> 4);
        System.out.println(a >> 5);

        System.out.println(a << 1);
        System.out.println(a << 2);
        System.out.println(a << 3);
        System.out.println(a << 4);
        System.out.println(a << 5);
    }
}
